'use strict';

var numero = 1;
var numero2 = 5;

//Operadores basicos

//suma
var rs = numero + numero2;
//resta
var rr = numero - numero2;
//division
var rd = numero / numero2;
//multiplicacion
var rm = numero * numero2;

console.log(numero);
console.log(numero2);
console.log("El resultado de la suma es: " + rs);
console.log("El resultado de la resta es: " + rr);
console.log("El resultado de la division es: " + rd);
console.log("El resultado de la multiplicacion es: " + rm);

//operadores de incremento

rs ++;
